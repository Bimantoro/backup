								<p class="mb-none text-light" style="text-align:justify">
								As an umbrella of universal human rights, International principles of Human Rights were constructed by the representatives of the countries worldwide and are hoped to be ratified and applied globally. However, history has recorded that the approaches to their practice worldwide have varied. Not all human rights principles that are accepted as universal are applied consistently and in agreement with the description provided. Several countries have selectively applied human rights based on priorities or their political agenda, despite the fact that this selective practice is not encouraged by the UN human right system as all human rights are mutually important and reinforcing. The United States, a country known to promote human rights overseas, for example, has not applied the “equality of all human beings,” by not yet upgrading the law to ensure that.
